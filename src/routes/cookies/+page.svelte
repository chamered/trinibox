        <h4 class="text-warning mt-0 mb-3">1. Cosa sono i Cookie?</h4>
        <p>I cookie sono piccoli file di testo che i siti web salvano sul tuo computer o dispositivo mobile mentre li visiti. Servono a far funzionare i siti in modo più efficiente o a fornire informazioni ai proprietari del sito.</p>

        <h4 class="text-warning mt-4 mb-3">2. Quali Cookie utilizziamo?</h4>
        <p>Su TriniTalk ci teniamo alla tua privacy. <strong>Non utilizziamo cookie di profilazione o pubblicitari.</strong> Utilizziamo esclusivamente <strong>Cookie Tecnici (Essenziali)</strong>, senza i quali il sito non potrebbe funzionare correttamente. Nello specifico, usiamo:</p>
        
        <ul>
            <li class="mb-2"><strong>Cookie di Sessione e Autenticazione:</strong> Forniti da Supabase, ci servono per ricordare che hai fatto l'accesso al tuo account, permettendoti di votare le domande senza dover rimettere la password a ogni click.</li>
            <li><strong>Cookie di Consenso:</strong> Un piccolo file salvato nel tuo browser (chiamato LocalStorage) che ricorda semplicemente che hai cliccato "Ho capito" sul nostro banner dei cookie, per non mostratelo più a ogni visita.</li>
        </ul>

        <h4 class="text-warning mt-4 mb-3">3. Devo dare il consenso?</h4>
        <p>Secondo la normativa europea (GDPR) e le linee guida del Garante della Privacy, per l'utilizzo dei soli cookie tecnici <strong>non è richiesto il consenso preventivo</strong> dell'utente. Tuttavia, è nostro dovere informarti della loro presenza tramite questa pagina e l'apposito banner.</p>

        <h4 class="text-warning mt-4 mb-3">4. Come disabilitarli?</h4>
        <p>Puoi impostare il tuo browser per bloccare o avvisarti riguardo a questi cookie, ma in tal caso alcune parti del sito (come la possibilità di fare login o votare le domande) non funzioneranno.</p>
